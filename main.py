def plus(x, y):
    x, y = map(int, [x, y])
    print(x + y)


def diff(x, y):
    x, y = map(int, [x, y])
    print(x - y)


def mul(x, y):
    x, y = map(int, [x, y])
    print(x * y)


def div(x, y):
    x, y = map(int, [x, y])
    print(x / y)


def floor(x, y):
    x, y = map(int, [x, y])
    print(x // y)


def remainder(x, y):
    x, y = map(int, [x, y])
    print(x % y)


def power(x, y):
    x, y = map(int, [x, y])
    print(x**y)


